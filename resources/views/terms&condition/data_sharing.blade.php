        </ul>

        <h2>3. What Data is Shared</h2>
        <ul>
            <li>Public profile details (skills, certifications, work history).</li>
            <li>Resume or portfolio information you upload.</li>
            <li>Communication exchanges with employers/clients (via chat).</li>
        </ul>

        <h2>4. What Data is Not Shared</h2>
        <ul>
            <li>Private contact information (email, phone number) unless you choose to disclose it to an employer.</li>
        </ul>

        <h2>5. User Control</h2>
        <ul>
            <li>Users decide what information is visible on their profile.</li>
            <li>Users may edit or remove shared data anytime.</li>
            <li>Users can request restriction of sharing under certain conditions.</li>
        </ul>

        <h2>6. Accountability</h2>
        <p>Employers and clients receiving data must comply with this policy and use the information only for
            employment-related purposes.</p>

